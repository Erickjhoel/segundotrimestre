public class Bici extends Vehiculo{
    private int tamañorueda;
    private int nummarchas;
    private int tipodecambio;

    public int getTamañorueda() {
        return tamañorueda;
    }

    public void setTamañorueda(int tamañorueda) {
        this.tamañorueda = tamañorueda;
    }

    public int getNummarchas() {
        return nummarchas;
    }

    public void setNummarchas(int nummarchas) {
        this.nummarchas = nummarchas;
    }

    public int getTipodecambio() {
        return tipodecambio;
    }

    public void setTipodecambio(int tipodecambio) {
        this.tipodecambio = tipodecambio;
    }
    
    
    
    
    
    
    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public double getPreciocompra() {
        return preciocompra;
    }

    public void setPreciocompra(double preciocompra) {
        this.preciocompra = preciocompra;
    }

    public double getPrecioventa() {
        return precioventa;
    }

    public void setPrecioventa(double precioventa) {
        this.precioventa = precioventa;
    }
    
    
    
    
    
}
